/*******************************************************************************
* Function Name  : USART1_IRQHandler
* Description    : This function handles USART2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
u32 rxcnt = 0;
void USART1_IRQHandler(void)
{
    OS_CPU_SR  cpu_sr = 0;
    u8 tData,t=0;

	OS_ENTER_CRITICAL(); 	/* Tell uC/OS-II that we are starting an ISR */
    OSIntNesting++;
    OS_EXIT_CRITICAL();

	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)
	{
        rxcnt++;
		USART_ClearITPendingBit(USART1, USART_IT_RXNE);
        tData = (INT8U)(USART_ReceiveData(USART1) & 0xFF); 
        if(RxFlag == 0)
        {
            switch(RecStep)
            {
                case 0:
                    if(tData==0x3A)
                    {
                        RecStep = 1;
                        Rx1Head = 0;
                        Rxd1Buf[Rx1Head++] = tData;
                    }
                    else
                    {
                        RecStep = 0;
                        Rx1Head = 0;
                    }
                    break;
                case 1:
                    if(tData==0x5B)
                    {
                        RecStep = 2;
                        Rxd1Buf[Rx1Head++] = tData;
                    }
                    else
                    {
                        RecStep = 0;
                        Rx1Head = 0;
                    }
                    break;
                case 2:
                    if(tData==0x7C)
                    {
                        RecStep = 3;
                        Rxd1Buf[Rx1Head++] = tData;
                    }
                    else
                    {
                        RecStep = 0;
                        Rx1Head = 0;
                    }
                    break;
                case 3:
                    Rxd1Buf[Rx1Head++] = tData;
                    if(Rx1Head == 9)
                    {
                        t = ~Rxd1Buf[8];
                        if(Rxd1Buf[7] == t)
                        {
                            RecStep = 4;
                        }
                        else
                        {
                            RecStep = 0;
                            Rx1Head = 0;
                        }
                    }
                    break;
                case 4:
                    Rxd1Buf[Rx1Head++] = tData;
                    if(Rx1Head == (Rxd1Buf[7]+12))
                    {
                        RecStep = 0;
                        RxFlag = 1;//接收到有效数据
                    }
                    break;
                default:
                    break;
            }
        }
	}
    else if(USART_GetFlagStatus(USART1, USART_IT_ORE) == SET)
    {//清接收溢出标志，只能用读数据的方式来清溢出标志
        rxcnt++;
	    USART_ReceiveData(USART1);
    }
	OSIntExit();
}


/*******************************************************************************
* Function Name  : USART2_IRQHandler
* Description    : This function handles USART2 global interrupt request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
u8 tData = 0;
void USART2_IRQHandler(void)
{
	OS_CPU_SR  cpu_sr = 0;
	
	OS_ENTER_CRITICAL(); 
    OSIntNesting++;
    OS_EXIT_CRITICAL();

	if(USART_GetITStatus(USART2, USART_IT_RXNE) != RESET)
	{
		USART_ClearITPendingBit(USART2, USART_IT_RXNE);
        tData = (INT8U)(USART_ReceiveData(USART2) & 0xFF); 
	}
    else if(USART_GetFlagStatus(USART2, USART_IT_ORE) == SET)
    {//清接收溢出标志，只能用读数据的方式来清溢出标志
	    USART_ReceiveData(USART2);
    }
	OSIntExit();
}
